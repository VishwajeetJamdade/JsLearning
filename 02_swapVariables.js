var sweety="Sweety";
var cutie ="Cutie";
console.log("       ","Swapping Using Two Values");
console.log("       ","-------------------------");
console.log("Before Swapping","  ","sweety :",sweety, " ", "cutie : ",cutie );
var temp = sweety;
sweety=cutie;
cutie=temp;
console.log("After Swapping","  ","sweety :",sweety,"  ","cutie :",cutie );
console.log("*************************************************************");


